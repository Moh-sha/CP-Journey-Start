#include <bits/stdc++.h>
using namespace std;

int main(){
   int N;

   cin>>N;

   int h,m,s,re;

    h = N/3600;
   //h =0

    re = N%3600;

   //re=556

    m = re/60;

    re = re%60;

    s = re/1;


  cout << h<< ":" << m << ":" << s << endl;

  return 0;





}
